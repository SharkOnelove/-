import time

# รายการข้อความและเวลาหน่วง (เวลาหน่วงเป็นวินาที)
messages = [
    ("โชคดีเหลือเกินที่เราได้เจอกัน", 3),
    ("โชคดีที่เธอยังยอมอยู่ข้างฉัน", 3),
    ("ไหนใครว่าเวลาเปลี่ยนไป", 3),
    ("หัวใจใคร ๆ มักเปลี่ยนแปลง", 3),
    # เพิ่มข้อความ โดยใช้ ("ข้อความที่จะเพิ่ม", เวลาหน่วง 3 วิ),
]

# วนลูปเพื่อแสดงข้อความและหน่วงเวลา สำคัญห้ามเเก้ไข
for message, delay in messages:
    print(message)
    time.sleep(delay) #ฟังชั่นหน่วงเวลา

